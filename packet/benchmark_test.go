func BenchmarkParsePacket(b *testing.B) {
	b.ResetTimer()
	for i := 0; i < b.N; i++ {
		p, _ := ParsePacket([]byte{0x7E, 0x0A, 0x41, 0x00, 0x02, 0x00, 0x00, 0x0F, 0x0A, 0x0D, 0x31, 0x36, 0x36, 0x30, 0x33, 0x36, 0x30, 0x39, 0x31, 0x35, 0x35, 0x35, 0x36})
		PutPacket(p)
	}
}
